# Array to track visited nodes
visited = [[False for i in range(25)] for j in range(25)]


def isValid(y: int, x: int) -> bool:
    # check bounds
    if y < 0 or x < 0 or y > len(visited) - 1 or x > len(visited) - 1:
        return False

    # check for wall
    if maze[y][x] == 1:
        return False

    # ignore repeated states
    if visited[y][x]:
        return False

    return True


def print_maze() -> None:
    for row in maze:
        print(row)


def print_visited() -> None:
    for y in range(len(visited)):
        for x in range(len(visited)):
            if visited[y][x]:
                print("•", end="")
            elif maze[y][x] == 1:
                print("█", end="")
            else:
                print(" ", end="")
        print()


def count_visited() -> int:
    count = 0
    for y in range(len(visited)):
        for x in range(len(visited)):
            if visited[y][x]:
                count += 1
    return count


def bfs_search(start_pos: tuple, end_pos: tuple):
    y_dirs = (1, 0, -1, 0)
    x_dirs = (0, 1, 0, -1)

    fringe = []
    fringe.append(start_pos)
    visited[start_pos[0]][start_pos[1]] = True

    curr = start_pos
    while len(fringe) != 0:
        print(fringe)
        curr = fringe.pop(0)
        if curr == end_pos:
            print(f"\nFound goal after exploring {count_visited()} nodes.")
            # print(f"Final path")
            return
        curr_y = curr[0]
        curr_x = curr[1]

        # Add adjacent nodes to fringe
        for i in range(len(x_dirs)):
            y = curr_y + y_dirs[i]
            x = curr_x + x_dirs[i]
            if isValid(y, x):
                fringe.append((y, x))
                visited[y][x] = True

    print(f"\Goal not found after exloring {count_visited()} nodes.")


